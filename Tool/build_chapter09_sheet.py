def section_number(title: str) -> int | None:
    clean = title.strip()
    if clean == "ภาพรวมของบท":
        return 0
    match = re.match(r"^(\d+)[\.\)]\s*", clean)
    return int(match.group(1)) if match else None


def clean_section_title(title: str) -> str:
    return re.sub(r"^(\d+)[\.\)]\s*", "", title).strip()


