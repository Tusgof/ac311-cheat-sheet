def tone_for(title: str) -> str:
    if any(key in title for key in ["จุดที่ควรอ่านอย่างระวัง", "หลุมพราง", "ห้าม", "Warnings"]):
        return "trap"
    if any(key in title for key in ["กฎหลัก", "หลักที่ต้องจำ", "??????????????", "Probability Matrix", "ผลลัพธ์ปลายทาง"]):
        return "alert"
    if any(key in title for key in ["สรุป", "Matrix", "ลำดับการคิด", "บทสรุปสุดท้าย"]):
        return "summary"
    if "ตัวอย่าง" in title or "Atlas" in title or "Eco-Plant" in title:
        return "example"
    if any(key in title for key in ["เหตุผล", "หลัก", "แนวคิด", "ตาราง logic", "การประเมินซ้ำ"]):
        return "note"
    return "default"


def section_theme_class(title: str) -> str:
    lowered = title.lower()
    if any(key in title for key in ["Contingent", "Disclosure", "เปิดเผย"]):
        return "theme-disclosure"
    if any(key in title for key in ["Measurement", "Time Value", "Decommissioning"]) or "pv" in lowered:
        return "theme-accounting"
    if any(key in title for key in ["??????????????", "Probability Matrix", "บทสรุปสุดท้าย", "จุดที่ควรอ่านอย่างระวัง"]):
        return "theme-review"
    return "theme-default"


def build_sections(soup_root):
    page_title, sections = original_build_sections(soup_root)
    page_title = re.sub(r"^Chapter\s*\d+\s*:\s*", "", page_title).strip()
    return page_title, sections


