def main() -> None:
    soup = BeautifulSoup(HTML_PATH.read_text(encoding="utf-8"), "html.parser")

    for node in soup.find_all(string=True):
        if not isinstance(node, NavigableString):
            continue
        text = str(node)
        stripped = text.strip()
        if not stripped:
            continue
        if stripped in NODE_REPLACEMENTS:
            replacement = NODE_REPLACEMENTS[stripped]
            if text == stripped:
                node.replace_with(replacement)
            else:
                node.replace_with(text.replace(stripped, replacement))
            continue
        updated = text
        for old, new in SUBSTRING_REPLACEMENTS:
            updated = updated.replace(old, new)
        if updated != text:
            node.replace_with(updated)

    html = str(soup)
    html = html.replace("<title>AC311 | บทที่ 9: IAS 37 - ประมาณการหนี้สิน หนี้สินที่อาจเกิดขึ้น และสินทรัพย์ที่อาจเกิดขึ้น</title>",
                        "<title>AC311 | บทที่ 9: IAS 37 - ประมาณการหนี้สิน หนี้สินที่อาจเกิดขึ้น และสินทรัพย์ที่อาจเกิดขึ้น</title>")
    HTML_PATH.write_text(html, encoding="utf-8")


if __name__ == "__main__":
    main()
